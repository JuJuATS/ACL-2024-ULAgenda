


var current = null;
const userForm = document.querySelector(".UserForm");
userForm.addEventListener("submit",(e)=>{
  let password = document.querySelector("#password");
  let email = document.querySelector("#email");
  if(!password.value || !email.value){
    e.preventDefault()
  }
})
